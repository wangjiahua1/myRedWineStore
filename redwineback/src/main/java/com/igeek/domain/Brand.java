package com.igeek.domain;
//品牌类
public class Brand {
    int bid;
    String bname;

    public Brand() {
    }

    public int getBid() {
        return bid;
    }

    public void setBid(int bid) {
        this.bid = bid;
    }

    public String getBname() {
        return bname;
    }

    public void setBname(String bname) {
        this.bname = bname;
    }
}
